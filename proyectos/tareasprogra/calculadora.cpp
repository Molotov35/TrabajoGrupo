#include <iostream>

using namespace std;
int main()
{
    int p1, p2;
    cout << "Ingrese un numero: ";
    cin >> p1;
    cout << "Ingrese otro numero: ";
    cin >> p2;
    cout << "La suma de los numeros es:"<< p1 + p2 << end;
    cout << "La resta de los numeros es: " << p1 - p2 << end;
    cout << "La multipliación de los numeros es: " << p1 * p2 << end;
    cout << "La dicison de los numeros es: " << p1 / p2 << end;
    return 0;
}
